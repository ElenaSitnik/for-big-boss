public class Shelf {
    private String colour;
    private int length; //cm
    private int width; //cm
    private int thickness; //mm
    private double weight; //kg

    public Shelf(String colour, int length, int width, int thickness, double weight){
        this.colour = colour;
        this.length = length;
        this.width = width;
        this.thickness = thickness;
        this.weight = weight;
    }
}
